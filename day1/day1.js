const sorted = generatedArray.sort((a, b) => a - b);

const SmartSolution = () => {
  let target = 2020;

  let getResult = () => {
    for (index = 0; index < sorted.length; index++) {
      let left = index + 1;
      let right = sorted.length - 1;
      let sum = 0;

      while (left < right) {
        sum = sorted[index] + sorted[left] + sorted[right];

        if (sum === target) {
          console.log(
            `SmartSolution: index=${index}, left=${left}, right=${right}, sum=${sum}`
          );
          return sorted[index] * sorted[left] * sorted[right];
        } else if (sum < target) {
          ++left;
        } else {
          --right;
        }
      }
    }
  };

  let result = getResult();
  console.log('RESULT', result);
};

